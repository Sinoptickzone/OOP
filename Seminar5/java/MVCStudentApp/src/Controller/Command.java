package Controller;

/**
 * This enum represents commands that can be performed in the application.
 */
public enum Command {
    /**
     * No command
     */
    NONE,

    /**
     * Read command
     */
    READ,

    /**
     * Create command
     */
    CREATE,

    /**
     * Update command
     */
    UPDATE,

    /**
     * List command
     */
    LIST,

    /**
     * Delete command
     */
    DELETE,

    /**
     * Exit command
     */
    EXIT
}
