package task2;

/**
 * A utility class for calculating allowed speed of the vehicle.
 */
public class SpeedCalculation {
    /**
     * Calculates the allowed speed for a vehicle.
     *
     * @param vehicle vehicle for calculating allowed speed
     * @return allowed speed for a certain vehicle
     */
    public static double calculateAllowedSpeed(Vehicle vehicle) {
        return vehicle.calculateAllowedSpeed();
    }
}
